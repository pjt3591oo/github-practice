const State = {
  web3: new Web3(new Web3.providers.HttpProvider(ETHEREUM_URL)),
  pk: '',
  account: null,
  contract: null,

  getInstance (pk, account, contract) {
    if(!this.pk) this.pk = pk;
    if(!this.account) this.account = account;
    if(!this.contract) this.contract = contract;

    return {
      pk: this.pk,
      account: this.account,
      contract: this.contract,
      web3: this.web3,
    }
  },

	setPrivateKey() {
		this.pk = document.getElementById('pk').value;
		if(!pk) return;
		this.account = this.web3.eth.accounts.privateKeyToAccount(this.pk)
	},

	setContract() {
    const ca = document.getElementById('ca').value;
		if(!ca) return;
		this.contract = new this.web3.eth.Contract(ABI, ca);
	}
}
