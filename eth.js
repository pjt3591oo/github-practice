function parserUrl() {
	const splites = window.location.search.split('?');

	return splites.length > 1 && splites[1]
		.split('&')
		.reduce((acc, cur) => {
			const [key, value] = cur.split('=');
			acc[key] = value;
			return acc
		}, {});
}

