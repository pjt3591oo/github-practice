let contract = new web3.eth.Contract(ABI, CA)

async function pkToAccount(pk) {
  const { address } = await web3.eth.accounts.privateKeyToAccount(pk);
  const ethBalance = await web3.eth.getBalance(address);

  const nftBalance = await contract.methods.balanceOf(address).call();

  return {
    ethBalance: web3.utils.fromWei(ethBalance, 'ETHER'),
    nftBalance,
  }
}

async function mint(author, file) {
  if (!pk) {
    alert('NFT 발행을 위해 개인키를 가지고 와야합니다.')
    return;
  }
  const account = await web3.eth.accounts.privateKeyToAccount(pk);
  const datetime = new Date().getTime();
  const rst = await ipfs.add(file);

  const bytecode = await contract.methods.mint(account.address, author, datetime.toString(), rst.path).encodeABI();
  const tx = {
    from: account.address,
    to: CA,
    gas: 1000000,
    gasPrice: '21000000000',
    data: bytecode
  }

  const signedTx = await account.signTransaction(tx);
  const sendTx = await web3.eth.sendSignedTransaction(signedTx.rawTransaction);

  return true;
}


async function findNft(tokenId) {
  const {author, datetime, url} = await contract.methods.images(tokenId).call();

  let binaryData = null;

  for await (const i of ipfs.cat(url)) {
    binaryData = i;
  }

  return {
    author,
    datetime: new Date(parseInt(datetime)),
    url: await URL.createObjectURL(
      new Blob([binaryData.buffer], { type: 'image/png' } )
    ),
  }
}

